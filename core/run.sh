#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# run.sh — запуск Free Karaoke в отдельном окне (pywebview + PyQt6)
# ─────────────────────────────────────────────────────────────────────────────

DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )"
cd "$DIR"

# ─────────────────────────────────────────────────────────────────────────────
# 1. Проверка окружения (ищем .venv в core/ или на уровень выше)
# ─────────────────────────────────────────────────────────────────────────────
VENV_DIR="$DIR/.venv"
if [ ! -d "$VENV_DIR" ]; then
    # Пробуем на уровень выше (наша новая структура установки)
    PARENT_DIR="$(cd "$DIR/.." && pwd)"
    if [ -d "$PARENT_DIR/.venv" ]; then
        VENV_DIR="$PARENT_DIR/.venv"
    else
        echo "❌ Виртуальное окружение не найдено."
        echo "   Запустите установщик заново."
        exit 1
    fi
fi

# ─────────────────────────────────────────────────────────────────────────────
# 2. Загружаем переменные изоляции кэша (Там же теперь и фиксы для AMD)
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env.cache" ]; then
    set -a
    source "$DIR/.env.cache"
    set +a
else
    echo "⚠️  .env.cache не найден — кэши могут писаться в системные папки"
fi

# ─────────────────────────────────────────────────────────────────────────────
# 3. Загружаем .env (токены и настройки приложения)
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env" ]; then
    set -a
    source "$DIR/.env"
    set +a
else
    echo "❌ Файл .env не найден!"
    echo "   Без него приложение не запустится."
    echo "   Скопируй шаблон и заполни токены:"
    echo "   cp .env.example .env"
    echo "   Затем открой .env и вставь свои ключи."
    exit 1
fi

# ─────────────────────────────────────────────────────────────────────────────
# 4. Активируем виртуальное окружение
# ─────────────────────────────────────────────────────────────────────────────
source "$VENV_DIR/bin/activate"

# ─────────────────────────────────────────────────────────────────────────────
# 5. Создаём нужные папки (Строгая портативность)
# ─────────────────────────────────────────────────────────────────────────────
mkdir -p "$DIR/library"
mkdir -p "$DIR/models/whisper"
mkdir -p "$DIR/models/audio_separator"
mkdir -p "$DIR/models/torch"
mkdir -p "$DIR/cache/uv"
mkdir -p "$DIR/debug_logs"

# ─────────────────────────────────────────────────────────────────────────────
# 6. Запускаем launcher.py — он сам поднимет сервер и откроет окно
# ─────────────────────────────────────────────────────────────────────────────
python "$DIR/launcher.py"