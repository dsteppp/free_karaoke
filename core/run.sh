#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# run.sh — запуск Free Karaoke в отдельном окне (pywebview + PyQt6)
# ─────────────────────────────────────────────────────────────────────────────

DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )"
cd "$DIR"

# ─────────────────────────────────────────────────────────────────────────────
# 1. Проверка окружения (ищем .venv в core/ или на уровень выше)
# ─────────────────────────────────────────────────────────────────────────────
VENV_DIR="$DIR/.venv"
if [ ! -d "$VENV_DIR" ]; then
    # Пробуем на уровень выше (старая структура репозитория)
    PARENT_DIR="$(cd "$DIR/.." && pwd)"
    if [ -d "$PARENT_DIR/.venv" ]; then
        VENV_DIR="$PARENT_DIR/.venv"
        echo "⚠️  .venv найден на уровень выше: $VENV_DIR"
    else
        echo "❌ Виртуальное окружение не найдено."
        echo "   Запустите: bash reinstall.sh"
        exit 1
    fi
fi

# ─────────────────────────────────────────────────────────────────────────────
# 2. Загружаем переменные изоляции кэша
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env.cache" ]; then
    set -a
    source "$DIR/.env.cache"
    set +a
else
    echo "⚠️  .env.cache не найден — кэши могут писаться в систему"
    echo "   Рекомендуется запустить reinstall.sh"
fi

# ─────────────────────────────────────────────────────────────────────────────
# 2.5. QtWebEngine/Chromium: принудительная настройка для стабильности UI
# ВАЖНО: Должно быть ДО активации venv и ДО запуска Python
# ─────────────────────────────────────────────────────────────────────────────
_webview_cache="$DIR/cache/webview"
_chromium_cache="$DIR/cache/chromium"
mkdir -p "$_webview_cache" "$_chromium_cache"

export QTWEBENGINE_CHROMIUM_FLAGS="--no-sandbox --disable-gpu-sandbox --disable-dev-shm-usage --disable-http-cache --disk-cache-dir=$_chromium_cache --disk-cache-size=0 --disable-gl-drawing-for-tests"
export QT_QPA_PLATFORM=xcb
export QTWEBENGINE_DICTIONARIES_PATH="$_chromium_cache"

# ─────────────────────────────────────────────────────────────────────────────
# 2.6. PyTorch ROCm: маппим gfx1102 (RX 7600 XT) → gfx1100 для TensileLibrary
# ─────────────────────────────────────────────────────────────────────────────
export HSA_OVERRIDE_GFX_VERSION=11.0.0

# ─────────────────────────────────────────────────────────────────────────────
# 3. Загружаем .env (токены и настройки приложения)
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env" ]; then
    set -a
    source "$DIR/.env"
    set +a
else
    echo "❌ Файл .env не найден!"
    echo "   Без него приложение не запустится."
    echo "   Скопируй шаблон и заполни токены:"
    echo "   cp .env.example .env"
    echo "   Затем открой .env и вставь свои ключи."
    exit 1
fi

# ─────────────────────────────────────────────────────────────────────────────
# 4. Активируем виртуальное окружение
# ─────────────────────────────────────────────────────────────────────────────
source "$VENV_DIR/bin/activate"

# ─────────────────────────────────────────────────────────────────────────────
# 5. Создаём нужные папки если вдруг удалили
# ─────────────────────────────────────────────────────────────────────────────
mkdir -p "$DIR/library"
mkdir -p "$DIR/models/whisper"
mkdir -p "$DIR/cache/uv"
mkdir -p "$DIR/cache/torch"
mkdir -p "$DIR/cache/huggingface"
mkdir -p "$DIR/cache/chromium"
mkdir -p "$DIR/cache/webview"
mkdir -p "$DIR/debug_logs"

# ─────────────────────────────────────────────────────────────────────────────
# 6. Запускаем launcher.py — он сам поднимет сервер и откроет окно
# ─────────────────────────────────────────────────────────────────────────────
python "$DIR/launcher.py"
