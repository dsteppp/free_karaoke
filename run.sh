#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# run.sh — запуск AI-Karaoke Pro в отдельном окне (pywebview + PyQt6)
# ─────────────────────────────────────────────────────────────────────────────

DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )"
cd "$DIR"

# ─────────────────────────────────────────────────────────────────────────────
# 1. Проверка окружения
# ─────────────────────────────────────────────────────────────────────────────
if [ ! -d "$DIR/.venv" ]; then
    echo "❌ Виртуальное окружение не найдено."
    echo "   Запустите сначала: bash reinstall.sh"
    exit 1
fi

# ─────────────────────────────────────────────────────────────────────────────
# 2. Загружаем переменные изоляции кэша
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env.cache" ]; then
    set -a
    source "$DIR/.env.cache"
    set +a
else
    echo "⚠️  .env.cache не найден — кэши могут писаться в систему"
    echo "   Рекомендуется запустить reinstall.sh"
fi

# ─────────────────────────────────────────────────────────────────────────────
# 3. Загружаем .env (токены и настройки приложения)
# ─────────────────────────────────────────────────────────────────────────────
if [ -f "$DIR/.env" ]; then
    set -a
    source "$DIR/.env"
    set +a
fi

# ─────────────────────────────────────────────────────────────────────────────
# 4. Активируем виртуальное окружение
# ─────────────────────────────────────────────────────────────────────────────
source "$DIR/.venv/bin/activate"

# ─────────────────────────────────────────────────────────────────────────────
# 5. Создаём нужные папки если вдруг удалили
# ─────────────────────────────────────────────────────────────────────────────
mkdir -p "$DIR/library"
mkdir -p "$DIR/models/whisper"
mkdir -p "$DIR/cache/uv"
mkdir -p "$DIR/cache/torch"
mkdir -p "$DIR/cache/huggingface"
mkdir -p "$DIR/debug_logs"

# ─────────────────────────────────────────────────────────────────────────────
# 6. Запускаем launcher.py — он сам поднимет сервер и откроет окно
# ─────────────────────────────────────────────────────────────────────────────
python "$DIR/launcher.py"
